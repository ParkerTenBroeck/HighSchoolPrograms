    private void jPanel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseClicked
        
        if (evt.getButton() == 3) {
            jul = !jul;
            if (!jul) {
                MandelBrot_double.calculateScreen(null);
            }
        } else {
            double[] pos = getMouseFractPos(evt.getX(), evt.getY());
            
            double[] temp = MandelBrot_double.getBoundingBox();
            double scale = temp[0] / 1.3;
            
            MandelBrot_double.setBoundingBox(new double[]{scale, pos[0], pos[1]});
            
            MandelBrot_double.calculateScreen(null);
        }

    }//GEN-LAST:event_jPanel1MouseClicked

