    private void jPanel1MouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseMoved
        
        if (jul) {
            double[] pos = getMouseFractPos(evt.getX(), evt.getY());
            
            MandelBrot_double.calculateScreen(new ComplexNumber(pos[0], pos[1]));
        }
    }//GEN-LAST:event_jPanel1MouseMoved
    
    private double[] getMouseFractPos(int mouseXPos, int mouseYPos) {
        
        double[] temp = MandelBrot_double.getBoundingBox();
        
        double xPos = mouseXPos / (double) jPanel1.getWidth();
        xPos = (xPos - 0.5) * 2;
        double yPos = mouseYPos / (double) jPanel1.getHeight();
        yPos = (yPos - 0.5) * 2;

        //System.out.println(xPos + " " + yPos);
        double xDiff = (temp[0] * 2) - temp[0];
        double yDiff = (temp[0] * 2) - temp[0];
        
        xPos = (xPos * xDiff) + temp[1];
        yPos = (yPos * yDiff) + temp[2];
        
        return new double[]{xPos, yPos};
    }

